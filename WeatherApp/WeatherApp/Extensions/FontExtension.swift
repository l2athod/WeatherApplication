import Foundation
import UIKit

extension UIFont {
    static func robotoSlabMedium(ofSize: CGFloat) -> UIFont? {
        return UIFont(name: "RobotoSlab-Medium", size: ofSize)
    }
    
    static func robotoSlabLight(ofSize: CGFloat) -> UIFont? {
        return UIFont(name: "RobotoSlab-Light", size: ofSize)
    }
}
